    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            validartela();
            ELocatario locatario = new ELocatario();

            if (!txtCodigo.getText().isEmpty()) {
                locatario.setCodigo(Integer.parseInt(txtCodigo.getText()));
            }

            //String pessoaf = btnGTipo_pessoa.getSelection().getActionCommand();
            locatario.setNome(txtNome.getText());
            locatario.setEndereco(txtEndereco.getText());
            locatario.setTelefone(txtTelefone.getText());
            locatario.setTipoCNH(txtCNH.getText());
            if (txtCPF.isEnabled()) {
                locatario.setTipo_pessoa(txtCPF.getText());
            } else {
                locatario.setTipo_pessoa(txtCNPJ.getText());
            }

            locatario.setEmail(txtEmail.getText());

            NLocatario negocio = new NLocatario();
            negocio.salvar(locatario);

            JOptionPane.showMessageDialog(null, "Locatário cadastrado com sucesso");
            limpartela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

