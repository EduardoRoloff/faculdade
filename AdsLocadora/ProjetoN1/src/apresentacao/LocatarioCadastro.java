    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int resposta = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do Locatario?", "ADS Locadora Veículos", JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_NO_OPTION) {
                int codigo = Integer.parseInt(txtCodigo.getText());
                new NLocatario().excluir(codigo);
                JOptionPane.showMessageDialog(null, "Locatário excluido com sucesso");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            String codigo = JOptionPane.showInputDialog("" + "Digite o código do locatário para consulta");

            if (codigo == null || codigo.equals("")) {
                throw new Exception("Digite o código para consulta");
            }

            int intCodigo = Integer.parseInt(codigo);
            
            ELocatario locatario = new NLocatario().consultar(intCodigo);
            
            if (locatario.getCodigo()!= 0) {
                preencherTela(locatario);
            }else{
                JOptionPane.showMessageDialog(null, "Associado inexistente");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "É necessário informar um código válido!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

