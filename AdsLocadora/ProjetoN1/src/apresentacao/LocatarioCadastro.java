    private void validartela() throws Exception {
        if (txtNome.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo nome!");
        }
        if (txtEndereco.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo endereço!");
        }
        if (txtCNH.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo CNH!");
        }
        if (rbJuridica.isSelected()) {
            if (txtCNPJ.getText().isEmpty()) {
                throw new Exception("É necessário preencher o campo CNPJ!");
            }
        }
        if (rbFisica.isSelected()) {
            if (txtCPF.getText().isEmpty()) {
                throw new Exception("É necessário preencher o campo CPF!");
            }
        }
        if (txtEmail.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo email!");
        }
        if (txtTelefone.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo telefone!");
        }

    }

    private void limpartela() {
        txtNome.setText("");
        txtEndereco.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtCNH.setText("");
        txtCodigo.setText("");
        txtCNPJ.setText("");
        txtCPF.setText("");
    }

    private void preencherTela(ELocatario locatario) {
        txtCodigo.setText(locatario.getCodigo() + "");
        txtNome.setText(locatario.getNome());
        txtEndereco.setText(locatario.getEndereco());
        txtTelefone.setText(locatario.getTelefone());
        txtCNH.setText(locatario.getTipoCNH());
        txtEmail.setText(locatario.getEmail());
        txtCPF.setText(locatario.getTipo_pessoa());
        txtCNPJ.setText(locatario.getTipo_pessoa());

        btnExcluir.setEnabled(true);
    }

