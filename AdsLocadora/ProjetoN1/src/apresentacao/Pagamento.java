    private void validartela() throws Exception {
        if (txtCodigoLocatario.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo codígo do locatário!");
        }
        if (cmbFormaDePagamento.getSelectedIndex() == 0) {
            throw new Exception("É necessário preencher o campo Forma de Pagamento!");
        }
        if (txtValordoAluguel.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo valor do aluguel!");
        }
    }

    private void limpartela() {
        txtCodigoLocatario.setText("");
        cmbFormaDePagamento.setSelectedIndex(0);
        txtValordoAluguel.setText("");
    }

    private void preencherTela(ELocacao locacao) {
        txtNome.setText(locacao.getLocatario().getNome());
        btnPagamento.setEnabled(true);
    }
