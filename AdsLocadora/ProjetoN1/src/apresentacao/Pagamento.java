    private void btnPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagamentoActionPerformed
        // TODO add your handling code here:
        try {
            validartela();

            EPagamento pagamento = new EPagamento();

            int codigo = Integer.parseInt(txtCodigoLocatario.getText());
            double valor = Double.parseDouble(txtValordoAluguel.getText());

            if (!txtCodigoLocatario.getText().isEmpty()) {
                pagamento.getCodigolocatario().setCodigo(codigo);
            }
            pagamento.setFormadepagamento((String) cmbFormaDePagamento.getSelectedItem());
            pagamento.setValorpagamento(valor);

            NPagamento negocio = new NPagamento();
            negocio.pagamento(pagamento);

            JOptionPane.showMessageDialog(null, "Pagamento efetuado com sucesso");
            limpartela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnPagamentoActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            int codigo = Integer.parseInt(txtCodigoLocatario.getText());

            ELocacao locacao = new NLocacao().consultar(codigo);
            if (locacao.getLocatario().getCodigo()!= 0) {
                preencherTela(locacao);
            } else {
                JOptionPane.showMessageDialog(null, "Associado não tem carro alugado");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Pagamento.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

