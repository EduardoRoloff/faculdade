    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            validartela();
            ECarro carro = new ECarro();

            if (!txtCodigo.getText().isEmpty()) {
                carro.setCodigo(Integer.parseInt(txtCodigo.getText()));
            }

            //String pessoaf = btnGTipo_pessoa.getSelection().getActionCommand();
            carro.setDirecao(txtDirecao.getText());
            carro.setNome_modelo(txtModelo.getText());
            carro.setMotor(txtMotor.getText());
            carro.setPlaca(txtPlaca.getText());
            carro.setPortas(txtPortas.getText());
            carro.setPassageiros(txtQdePassageiros.getText());
            carro.setValoraluguel(Double.parseDouble(txtValorAluguel.getText()));

            NCarro negocio = new NCarro();
            negocio.salvar(carro);

            JOptionPane.showMessageDialog(null, "Carro cadastrado com sucesso");
            limpartela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int resposta = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do Carro?", "ADS Locadora Veículos", JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_NO_OPTION) {
                int codigo = Integer.parseInt(txtCodigo.getText());
                new NLocatario().excluir(codigo);
                JOptionPane.showMessageDialog(null, "Carro excluido com sucesso");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarValorActionPerformed
        // TODO add your handling code here:
        try {
            ECarro carro = new ECarro();
            
            carro.setValoraluguel(Double.parseDouble(txtValorAluguel.getText()));
            
            NCarro negocio = new NCarro();
            negocio.alterar(Double.parseDouble(txtValorAluguel.getText()) , Integer.parseInt(txtCodigo.getText()));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAlterarValorActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            String codigo = JOptionPane.showInputDialog("" + "Digite o código do carro para consulta");

            if (codigo == null || codigo.equals("")) {
                throw new Exception("Digite o código para consulta");
            }

            int intCodigo = Integer.parseInt(codigo);
            
            ECarro carro = new NCarro().consultar(intCodigo);
            
            if (carro.getCodigo() != 0) {
                preencherTela(carro);
            }else{
                JOptionPane.showMessageDialog(null, "Carro inexistente");
            }
            
            btnAlterarValor.setEnabled(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "É necessário informar um código válido!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

