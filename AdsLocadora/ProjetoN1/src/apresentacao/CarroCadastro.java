    private void validartela() throws Exception {
        if (txtModelo.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo modelo do carro!");
        }
        if (txtPlaca.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo placa!");
        }
        if (txtValorAluguel.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo valor do aluguel!");
        }
        if (txtDirecao.getText().isEmpty()) {

            throw new Exception("É necessário preencher o campo direção!");

        }
        if (txtPortas.getText().isEmpty()) {

            throw new Exception("É necessário preencher o campo portas!");

        }
        if (txtQdePassageiros.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo passageiros!");
        }
        if (txtMotor.getText().isEmpty()) {
            throw new Exception("É necessário preencher o campo motor!");
        }
    }

    private void limpartela() {
        txtCodigo.setText("");
        txtDirecao.setText("");
        txtModelo.setText("");
        txtMotor.setText("");
        txtPlaca.setText("");
        txtPortas.setText("");
        txtQdePassageiros.setText("");
        txtValorAluguel.setText("");
    }

    private void preencherTela(ECarro carro) {
        txtCodigo.setText(carro.getCodigo() + "");
        txtDirecao.setText(carro.getDirecao());
        txtModelo.setText(carro.getNome_modelo());
        txtMotor.setText(carro.getMotor());
        txtPlaca.setText(carro.getPlaca());
        txtPortas.setText(carro.getPortas());
        txtQdePassageiros.setText(carro.getPassageiros());
        txtValorAluguel.setText(carro.getValoraluguel() + "");
        btnExcluir.setEnabled(true);
        btnAlterarValor.setEnabled(true);
    }
    
